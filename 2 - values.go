package main

import "fmt"

func main() {
	// Go has string
	// Plus sign concatenate one value to another without space
	fmt.Println("go" + "lang")

	// Also Go have integers and floats
	// Comma concatenate with space
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// Beside in Go we have booleans with OR, AND and NOT
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)

}